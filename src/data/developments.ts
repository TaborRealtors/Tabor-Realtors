const developmentPriority = [
  "citi-rise",
  "jasper-westlands-muthithi-road",
  "juja-rafi",
  "the-marquis",
];
developments.sort((a, b) => {
  const ai = developmentPriority.indexOf(a.slug);
  const bi = developmentPriority.indexOf(b.slug);
  if (ai === -1 && bi === -1) return 0;
  if (ai === -1) return 1;
  if (bi === -1) return -1;
  return ai - bi;
});
